#include "modulo_gps/TypeConverter.h"

using namespace std;

/**
 * Método de conversión double a string
 * @param[in] num Valor a convertir
 * @return Valor convertido
 */
string toString(double num){
  stringstream ss;
  ss << num;
  return ss.str();
}

/**
 * Método de conversión string a double
 * @param[in] num Valor a convertir
 * @return Valor convertido
 */
double stringToDouble(string s){
  stringstream ss;
  double d;
  ss << s;
  ss >> d;
  return d;
}

/**
 * Método de conversión string a float
 * @param[in] num Valor a convertir
 * @return Valor convertido
 */
float stringToFloat(string s){
  stringstream ss;
  float f;
  ss << s;
  ss >> f;
  return f;
}

/**
 * Método de conversión string a long
 * @param[in] num Valor a convertir
 * @return Valor convertido
 */
long stringToLong(string s){
  stringstream ss;
  long l;
  ss << s;
  ss >> l;
  return l;
}

/**
 * Método de conversión string a int
 * @param[in] num Valor a convertir
 * @return Valor convertido
 */
int stringToInt(string s){
  stringstream ss;
  int i;
  ss << s;
  ss >> i;
  return i;
}
